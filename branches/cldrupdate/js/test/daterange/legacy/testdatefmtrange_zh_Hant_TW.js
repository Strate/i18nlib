function testDateRngFmtTWRangeInDayShort() {
    var fmt = new ilib.DateRngFmt({locale: "zh-Hant-TW", length: "short"});
    assertNotNull(fmt);
    
    var start = new ilib.Date.GregDate({
		year: 2011,
		month: 12,
		day: 31,
		hour: 13,
		minute: 45,
		second: 0,
		millisecond: 0
	});
    var end = new ilib.Date.GregDate({
		year: 2011,
		month: 12,
		day: 31,
		hour: 14,
		minute: 30,
		second: 0,
		millisecond: 0
	});
    assertEquals("2011/12/31 下午1:45至下午2:30", fmt.format(start, end));
}
