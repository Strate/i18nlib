function testDateFmtSimpleShort_et_EE() {
    var fmt = new ilib.DateFmt({locale: "et-EE", length: "short"});
    assertNotNull(fmt);
    
    var date = new ilib.Date.GregDate({
		year: 2011,
		month: 9,
		day: 29,
		hour: 13,
		minute: 45,
		second: 0,
		millisecond: 0
	});
    assertEquals("29.9.11", fmt.format(date));
}

